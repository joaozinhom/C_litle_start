#include <stdio.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	if (to_find[0] == '\0')
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (str[i + j] == to_find[j] && to_find[j] != '\0')
		{
			j++;
		}
		if (to_find[j] == '\0')
		{
			return (&str[i]);
		}
		i++;
	}
	return (0);
}


int main()
{
	char text[] = "Learning C is is fun!";
	char *ptr = ft_strstr(text, "is");

	if (ptr)
		printf("Found: %s\n", ptr);
	else
		printf("Not found.\n");
	return 0;
}
